const cardInput = document.querySelector('.card-number-input');

cardInput.addEventListener('input', (event) => {
    const input = event.target.value.replace(/\D/g, ''); // Sadece rakamları al
    const formattedInput = formatCardNumber(input); // Formatlama fonksiyonunu çağır
    cardInput.value = formattedInput;
});

function formatCardNumber(input) {
    const groups = input.match(/.{1,4}/g);
    if (groups) {
        return groups.join('-');
    }
    return '';
}
